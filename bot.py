@dp.message(StateFilter(SendMessageStates.waiting_for_message))
async def process_message_to_send(message: types.Message, state: FSMContext):
    """–û–±—Ä–∞–±–æ—Ç–∫–∞ —Å–æ–æ–±—â–µ–Ω–∏—è –¥–ª—è —Ä–∞—Å—Å—ã–ª–∫–∏"""
    # –°–æ—Ö—Ä–∞–Ω—è–µ–º –¥–∞–Ω–Ω—ã–µ —Å–æ–æ–±—â–µ–Ω–∏—è
    message_data = {
        "text": message.text or message.caption or "",
        "photo": None
    }
    
    # –ï—Å–ª–∏ –µ—Å—Ç—å —Ñ–æ—Ç–æ
    if message.photo:
        # –ë–µ—Ä–µ–º —Ñ–æ—Ç–æ –Ω–∞–∏–±–æ–ª—å—à–µ–≥–æ —Ä–∞–∑–º–µ—Ä–∞
        photo = message.photo[-1]
        message_data["photo"] = photo.file_id
    
    # –°–æ—Ö—Ä–∞–Ω—è–µ–º –≤ —Å–æ—Å—Ç–æ—è–Ω–∏–µ
    await state.update_data(message_data=message_data)
    
    # –ö–Ω–æ–ø–∫–∏ –ø–æ–¥—Ç–≤–µ—Ä–∂–¥–µ–Ω–∏—è
    confirm_keyboard = InlineKeyboardMarkup(inline_keyboard=[
        [InlineKeyboardButton(text="‚úÖ –û—Ç–ø—Ä–∞–≤–∏—Ç—å", callback_data="confirm_send")],
        [InlineKeyboardButton(text="‚ùå –û—Ç–º–µ–Ω–∏—Ç—å", callback_data="cancel_send")]
    ])
    
    # –ü–æ–∫–∞–∑—ã–≤–∞–µ–º –ø—Ä–µ–≤—å—é —Å–æ–æ–±—â–µ–Ω–∏—è
    preview_text = "üìã –ü—Ä–µ–≤—å—é —Å–æ–æ–±—â–µ–Ω–∏—è:\n\n"
    if message_data["photo"]:
        preview_text += "üì∑ [–§–æ—Ç–æ]\n"
    preview_text += message_data["text"]
    preview_text += "\n\n–û—Ç–ø—Ä–∞–≤–∏—Ç—å —ç—Ç–æ —Å–æ–æ–±—â–µ–Ω–∏–µ –≤—Å–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è–º?"
    
    if message_data["photo"]:
        await message.answer_photo(
            photo=message_data["photo"],
            caption=preview_text,
            reply_markup=confirm_keyboard
        )
    else:
        await message.answer(
            preview_text,
            reply_markup=confirm_keyboard
        )
    
    await state.set_state(SendMessageStates.confirm_send)


